    /**
     * Metodo que maneja el evento del botón entrar para buscar al paciente
     * @param evt 
     */
    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        //Obtenemos el documento
        String documento = txtDocumento.getText();
        
        //Buscamos al paciente
        Paciente paciente = controlador.buscarPaciente(documento);
        
        if( paciente != null ){
            VistaPaciente vista = new VistaPaciente(controlador, paciente);
            vista.setVisible(true);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Documento no válido!");
        }
    }//GEN-LAST:event_btnEntrarActionPerformed

